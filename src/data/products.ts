export const getProductsByCategory = (cat: ProductCategory) =>
  products.filter((p) => p.category === cat);

const HOME_FEATURED_TARGETS: Record<ProductCategory, number> = {
  men: 20,
  women: 7,
  devices: 3,
};

/**
 * خلط ذكي لمنتجات الصفحة الرئيسية:
 * - يعرض 30 منتجاً إجمالاً (20 رجال + 7 نساء + 3 أجهزة)
 * - يعطي أولوية للمنتجات المحددة بـ featured ثم يكمّل من باقي منتجات نفس التصنيف
 * - يتجنّب ظهور نفس التصنيف مرتين متتاليتين كلّما أمكن
 * - النتيجة ثابتة (deterministic) — نفس الترتيب في كل refresh
 */
export const getFeaturedProducts = (): Product[] => {
  const groups = new Map<ProductCategory, Product[]>();

  for (const [cat, target] of Object.entries(HOME_FEATURED_TARGETS) as [
    ProductCategory,
    number,
  ][]) {
    const categoryProducts = getProductsByCategory(cat);
    const selected = [
      ...categoryProducts.filter((p) => p.featured),
      ...categoryProducts.filter((p) => !p.featured),
    ].slice(0, target);

    if (selected.length > 0) groups.set(cat, selected);
  }

  const total = [...groups.values()].reduce((sum, arr) => sum + arr.length, 0);
  const targets = new Map<ProductCategory, number>();
  const placed = new Map<ProductCategory, number>();
  for (const [cat, arr] of groups) {
    targets.set(cat, arr.length);
    placed.set(cat, 0);
  }

  const result: Product[] = [];
  let last: ProductCategory | null = null;

  while (result.length < total) {
    const pos = result.length + 1;

    // احسب "deficit" لكل تصنيف: المتوقّع - الموجود فعلاً
    const candidates: { cat: ProductCategory; deficit: number }[] = [];
    for (const [cat, arr] of groups) {
      if (arr.length === 0) continue;
      const expected = (targets.get(cat)! * pos) / total;
      const deficit = expected - placed.get(cat)!;
      candidates.push({ cat, deficit });
    }

    if (candidates.length === 0) break;

    // الأعلى deficit أولاً
    candidates.sort((a, b) => b.deficit - a.deficit);

    // اختر أعلى deficit مع تجنّب نفس التصنيف السابق إن أمكن
    const chosen = candidates.find((c) => c.cat !== last)?.cat ?? candidates[0].cat;

    result.push(groups.get(chosen)!.shift()!);
    placed.set(chosen, placed.get(chosen)! + 1);
    last = chosen;
  }

  return result;
};

